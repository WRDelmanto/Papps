package com.wrdelmanto.papps.utils

/**
 * Round to 1 decimals.
 */
fun roundTo1Decimals(number: Double): String = "%.1f".format(number)

/**
 * Round to 2 decimals.
 */
fun roundTo2Decimals(number: Double): String = "%.2f".format(number)

/**
 * Round to 3 decimals.
 */
fun roundTo3Decimals(number: Double): String = "%.3f".format(number)

/**
 * Round to 4 decimals.
 */
fun roundTo4Decimals(number: Double): String = "%.4f".format(number)